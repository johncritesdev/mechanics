//conditional chaining

let num = 0;
let firstName = "Jay";

//logical & operator -- both must be true
if (num === 0 && firstName === "Jay") {
  console.log("this is a truthy");
} else {
  console.log("this is a falsey");
}
//logical or operator -- at least one must be true
if (num === 1 || firstName === "Jay") {
  console.log("this is a truthy");
} else {
  console.log("this is a falsey");
}



